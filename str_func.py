def uppercase_string(input_string):
    """
    Принимаем строку и возвращаем её в верхнем регистре
    """
    return input_string.upper()

def capitalize_first_letters(input_string):
    """
    Принимаем строку и возвращаем её с заглавными первыми буквами каждого слова

    :param input_string:
    :return:
    """
    return input_string.title()
